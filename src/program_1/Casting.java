package program_1;

public class Casting {
	
	public static void main(String args[]) {
		
		int fruit=40;
		
		double vegitable=(double)fruit;
		System.out.println(vegitable);//int to double
		
		
		byte fish=(byte) vegitable;
		System.out.println(fish);
		
		
		String name ="Aryan";
		System.out.println("My son name is: "+name);//concatenation
		
		float age=7;
		byte month= (byte)age;
		System.out.println("His age is: "+month);//float to byte
		
		double day= (double)age;
		System.out.println(day);
		
		String name1="Abdullah";
		System.out.println("First name: "+name1);//concatenation
		
		
		
		
		
		
	}
	
	
	
	
	
	

}
